package com.ifa.bean;

public class TeamsBean {
	private String teamName, teamCode;
	private int mPlayed, mWon, mLost, mTie;
	public String getTeamName() {
		return teamName;
	}
	public void setTeamName(String teamName) {
		this.teamName = teamName;
	}
	public String getTeamCode() {
		return teamCode;
	}
	public void setTeamCode(String teamCode) {
		this.teamCode = teamCode;
	}
	public int getmPlayed() {
		return mPlayed;
	}
	public void setmPlayed(int mPlayed) {
		this.mPlayed = mPlayed;
	}
	public int getmWon() {
		return mWon;
	}
	public void setmWon(int mWon) {
		this.mWon = mWon;
	}
	public int getmLost() {
		return mLost;
	}
	public void setmLost(int mLost) {
		this.mLost = mLost;
	}
	public int getmTie() {
		return mTie;
	}
	public void setmTie(int mTie) {
		this.mTie = mTie;
	}
}
